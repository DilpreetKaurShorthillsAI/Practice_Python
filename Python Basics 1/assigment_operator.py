# Initialize variables
x = 10
y = 5

# Using various augmented assignment operators
x += 2   # x = x + 2  → x becomes 12
y -= 1   # y = y - 1  → y becomes 4
x *= 3   # x = x * 3  → x becomes 36
y /= 2   # y = y / 2  → y becomes 2.0
x //= 4  # x = x // 4 → x becomes 9
y %= 3   # y = y % 3  → y becomes 2.0
x **= 2  # x = x ** 2 → x becomes 81

print(f"x: {x}, y: {y}")  # Output: x: 81, y: 2.0
