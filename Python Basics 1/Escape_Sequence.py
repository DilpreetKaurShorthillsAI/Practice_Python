# Using various escape sequences
example_string = "Hello,\n\tThis is an example string with various escape sequences:\n"
example_string += "1. Backslash: \\\n"
example_string += "2. Single quote: \'\n"
example_string += "3. Double quote: \"\n"
example_string += "4. Newline: \n"
example_string += "5. Tab: \tIndented text\n"

print(example_string)
